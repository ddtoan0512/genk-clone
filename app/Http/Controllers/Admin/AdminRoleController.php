     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $this->authorize('update', Role::class);
        $role = Role::findOrFail($id);
        $permissions = Permission::all();
        return view("admin.role.edit", compact('role', 'permissions'));
    }

